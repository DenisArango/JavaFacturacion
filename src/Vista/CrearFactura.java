    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            String buscar = txtBuscar.getText().toLowerCase();

            conn = cn.getConection();
            qry = "SELECT BARCODE_ID, DESCRIPCION, STOCK, PRECIO_UNITARIO, ID_PROVEEDOR, NOMBRE FROM GET_PRODUCTOS() WHERE LOWER(DESCRIPCION) LIKE'%"+ buscar +"%'";
            st = conn.prepareStatement(qry);
            rs = st.executeQuery();

            DefaultTableModel modelProductos = new DefaultTableModel(){
                @Override
                public boolean isCellEditable(int row, int column){
                    return false;
                }
            };

            modelProductos.addColumn("Codigo de Barras");
            modelProductos.addColumn("Descripcion");
            modelProductos.addColumn("Stock");
            modelProductos.addColumn("Precio Unitario");
            modelProductos.addColumn("ID Proveedor");
            modelProductos.addColumn("Proveedor");
            tblProductos.setModel(modelProductos);

            String [] datos = new String[6];

            while(rs.next()){
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                modelProductos.addRow(datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CrearFactura.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

