    public void cargarTotal(){
        float total = 0;
        for(int i = 0; i < modelDetalle.getRowCount(); i++){
            total += Float.parseFloat(tblDetalle.getValueAt(i, 3).toString());
        }
        lblTotal1.setText(String.valueOf(total));
    }
    
    private void cargarClientes(){
        try {
            conn = cn.getConection();
            qry = "SELECT NOMBRE, APELLIDO, RAZON_SOCIAL FROM GET_CLIENTES()";
            st = conn.prepareStatement(qry);
            rs = st.executeQuery();
            while(rs.next()){
                String nombre = rs.getString(1);
                String apellido = rs.getString(2);
                String razon = rs.getString(3);
                if(nombre.equals("")){
                    modelcmbClientes.addElement(" - -" + razon);
                }else if(razon.equals("")){
                    modelcmbClientes.addElement(nombre + "-" + apellido + "- ");
                }else{
                    modelcmbClientes.addElement(nombre + "-" + apellido + "-" + razon);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(CrearFactura.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
